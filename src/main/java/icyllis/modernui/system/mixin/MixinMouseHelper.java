package icyllis.modernui.system.mixin;

import icyllis.modernui.system.ModernUI;
import net.minecraft.client.MouseHelper;
import org.spongepowered.asm.mixin.Mixin;
import org.spongepowered.asm.mixin.injection.At;
import org.spongepowered.asm.mixin.injection.Inject;
import org.spongepowered.asm.mixin.injection.callback.CallbackInfo;

@Mixin(MouseHelper.class)
public abstract class MixinMouseHelper {

    @Inject(method = "lambda$null$4", at = @At("HEAD"))
    private void mouse(long window, int button, int action, int modifiers, CallbackInfo ci) {
        ModernUI.LOGGER.debug("MouseButton: {}, {}, {}", button, action, modifiers);
    }
}
